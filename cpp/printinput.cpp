# include <bits/stdc++.h> // 헤더 파일을 include 시키다

using namespace std; // std 라는 네임스페이스를 사용한다. 변수명에 범위를 걸어 놓는 것
// std::cin 이렇게 써야하는걸 cin만으로 호출할 수 있게 해줌 

string a; //  문자열 변수 선언 <타입> <변수명>으로 선언함 
int main() {
	cin >> a; // 입력받기 input 
	cout << a << "\n"; // print
	return 0; // 프로세스를 정상적으로 마무리 한다는 의미 
}
